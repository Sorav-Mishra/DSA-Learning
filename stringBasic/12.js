// 12. Convert a String to Lowercase
// Write a function to convert all characters in a string to lowercase.

function convertStringLowercase(str) {
  return str.toLowerCase();
}
console.log(convertStringLowercase("ABHISHKSFSKFJH FJSKFSJFSDFJSF"));

// 13. Convert a String to Uppercase
// Write a function to convert all characters in a string to uppercase.

function convertStringUppercase(str2) {
  return str2.toUpperCase();
}
console.log(convertStringUppercase("abhishekfesfshfisjkfsf"));
